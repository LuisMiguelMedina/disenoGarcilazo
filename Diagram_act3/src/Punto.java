
public class Punto {
    private int x;
    private int y;

    public Punto(){ }

    public Punto(int _x, int _y){
        this.x = _x;
        this.y = _y;
    }

    public int getX() {
        return x;
    }

    public void setX(int _x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int _y) {
        this.y = y;
    }

    
}
